class Solution{
public:
    int evenlyDivides(int N){
        int no=N;
        int count=0;
        while(no)
        {
            int digit=no%10;
            if(digit!=0)
            {
            if(N%digit==0)
            count++;
            }
            no=no/10;
        }
        return count;
    }
};

